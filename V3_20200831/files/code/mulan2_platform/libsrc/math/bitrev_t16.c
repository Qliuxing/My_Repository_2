#include <typelib.h>

/* nibble reverse lookup table */
const uint8 bitrev4_16[16]=
{
	0x0, /* 0000 -> 0000 */
	0x8, /* 0001 -> 1000 */
	0x4, /* 0010 -> 0100 */
	0xc, /* 0011 -> 1100 */
	0x2, /* 0100 -> 0010 */
	0xa, /* 0101 -> 1010 */
	0x6, /* 0110 -> 0110 */
	0xe, /* 0111 -> 1110 */
	0x1, /* 1000 -> 0001 */
	0x9, /* 1001 -> 1001 */
	0x5, /* 1010 -> 0101 */
	0xd, /* 1011 -> 1101 */
	0x3, /* 1100 -> 0011 */
	0xb, /* 1101 -> 1011 */
	0x7, /* 1110 -> 0111 */
	0xf  /* 1111 -> 1111 */
};

/* EOF */
